using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MinhaBiblioteca
{
    public class CalculoDeArea
    {

        /// <summary>
        /// Metodo pra retornar a área de um quadrado
        /// </summary>
        /// <param name="ladoQuadrado"></param>
        /// <returns></returns>
        public double AreaQuadrado(int ladoQuadrado)
        {
            return ladoQuadrado * ladoQuadrado;
        }

    }
